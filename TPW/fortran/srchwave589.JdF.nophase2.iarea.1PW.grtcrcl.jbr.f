c          parph2cor1=1.                                      
c	  itypst2=itypst                                     
	 endif                                               
        enddo
	endif                                                


          cosph1 = cos(prefase1*twopi)
          cosph2 = cos(prefase2*twopi)
          sinph1 = sin(prefase1*twopi)
          sinph2 = sin(prefase2*twopi)
          
          prereal = staamp1*cosph1+staamp2*cosph2
