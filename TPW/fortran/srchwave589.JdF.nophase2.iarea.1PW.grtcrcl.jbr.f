	     wgtnode1(ista,ii,ideg)    = 0.
	     ampwgtnode1(ista,ii,ideg) = 0.  

 	   else

             wgtnode1(ista,ii,ideg) = sensitivity(ixindex,iyindex)
             ampwgtnode1(ista,ii,ideg) =     ampsens(ixindex,iyindex)
           endif 
	  else 
	     wgtnode1(ista,ii,ideg)    = 0.
	     ampwgtnode1(ista,ii,ideg) = 0.  
           endif	   
          enddo

	 dphase(ista,ideg) = 0.
         dampper(ista,ideg) = 0.
