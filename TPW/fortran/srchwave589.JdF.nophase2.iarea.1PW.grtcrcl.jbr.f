c        parph1cor1=0.                                        
c        parph2cor1=0.                                        
	 
c         itypst2=0 
	if (ntyp1.ne.0) then
        do itypst=1, ntyp1                                    
         if (istanum(iev,ista).eq.ityp1sta(itypst)) then     
          prefase1=prefase1+phcor(itypst)                    
          prefase2=prefase2+phcor(itypst)                    
