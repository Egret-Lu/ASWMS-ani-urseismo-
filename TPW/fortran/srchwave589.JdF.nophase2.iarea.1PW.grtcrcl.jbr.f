        do 20 ista = 1, nsta(iev)
         
       xstatemp = 
     1   xsta(iev,ista)*cos(stazim2(iev))
     1 + ysta(iev,ista)*sin(stazim2(iev))               
     
   	dtime2(ista) = (xstatemp-xmin(iev,ideg2))/unifvel

